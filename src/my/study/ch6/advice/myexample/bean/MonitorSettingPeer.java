package my.study.ch6.advice.myexample.bean;

public class MonitorSettingPeer {
	
	private Integer id;
	private String monitor_name;
	private Integer monitor_flg;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMonitor_name() {
		return monitor_name;
	}
	public void setMonitor_name(String monitor_name) {
		this.monitor_name = monitor_name;
	}
	public Integer getMonitor_flg() {
		return monitor_flg;
	}
	public void setMonitor_flg(Integer monitor_flg) {
		this.monitor_flg = monitor_flg;
	}

	
}
